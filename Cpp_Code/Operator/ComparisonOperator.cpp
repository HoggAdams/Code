#include <iostream>
using namespace std;

int main()
{
    // 比较运算符 --> 结果返回真或假
    int num1 = 1;
    int num2 = 2;

    cout << (num1 == num2) << endl;
    cout << (num1 != num2) << endl;
    cout << (num1 >= num2) << endl;
    cout << (num1 <= num2) << endl;
    cout << (num1 > num2) << endl;
    cout << (num1 < num2) << endl;

    return 0;
}
